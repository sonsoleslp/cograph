html_head <- function(title) {
  sprintf('<!DOCTYPE html>\n<html lang="en">\n<head>\n<meta charset="UTF-8">\n<meta name="viewport" content="width=device-width, initial-scale=1.0">\n<title>%s</title>\n%s\n</head>\n<body>\n<div class="container">\n',
          title, css_style)
}

html_tail <- function() {
  sprintf('<div class="footer">Generated by cograph validation suite &mdash; %s</div>\n</div>\n</body>\n</html>',
          format(Sys.time(), "%Y-%m-%d %H:%M:%S"))
}

html_header <- function(title, subtitle = "") {
  sub_html <- if (nzchar(subtitle)) sprintf('<div class="meta">%s</div>', subtitle) else ""
  sprintf('<div class="header"><h1>%s</h1>%s</div>\n', title, sub_html)
}

progress_bar_color <- function(pct) {
  if (pct >= 99) "green" else if (pct >= 80) "yellow" else "red"
}

html_summary_cards <- function(total, passed, failed, max_diff = NULL) {
  pct <- if (total > 0) round(100 * passed / total, 1) else 0
  bar_color <- progress_bar_color(pct)

  diff_card <- ""
  if (!is.null(max_diff) && !is.na(max_diff)) {
    diff_card <- sprintf(
      '<div class="card"><div class="label">Max Difference</div><div class="value neutral">%.2e</div></div>',
      max_diff)
  }

  sprintf(
    '<div class="summary-cards">
  <div class="card"><div class="label">Total Tests</div><div class="value neutral">%d</div></div>
  <div class="card"><div class="label">Passed</div><div class="value pass">%d</div></div>
  <div class="card"><div class="label">Failed</div><div class="value fail">%d</div></div>
  <div class="card"><div class="label">Pass Rate</div><div class="value %s">%.1f%%</div>
    <div class="progress-bar"><div class="progress-fill %s" style="width:%.1f%%"></div></div>
  </div>
  %s
</div>\n',
    total, passed, failed,
    if (pct >= 99) "pass" else "fail", pct,
    bar_color, pct,
    diff_card)
}

html_table <- function(df, highlight_col = NULL) {
  if (is.null(df) || nrow(df) == 0) return('<div class="no-data">No data available</div>\n')

  header <- paste0("<tr>", paste0("<th>", colnames(df), "</th>", collapse = ""), "</tr>")

  rows <- vapply(seq_len(nrow(df)), function(i) {
    cells <- vapply(seq_len(ncol(df)), function(j) {
      val <- as.character(df[i, j])
      if (!is.null(highlight_col) && colnames(df)[j] == highlight_col) {
        if (grepl("100", val) || grepl("PASS", val, ignore.case = TRUE)) {
          val <- sprintf('<span class="badge badge-pass">%s</span>', val)
        } else if (grepl("FAIL", val, ignore.case = TRUE) || grepl("^0", val)) {
          val <- sprintf('<span class="badge badge-fail">%s</span>', val)
        } else if (grepl("SKIP", val, ignore.case = TRUE)) {
          val <- sprintf('<span class="badge badge-skip">%s</span>', val)
        }
      }
      sprintf("<td>%s</td>", val)
    }, character(1))
    paste0("<tr>", paste0(cells, collapse = ""), "</tr>")
  }, character(1))

  sprintf("<table>\n<thead>%s</thead>\n<tbody>\n%s\n</tbody>\n</table>\n",
          header, paste0(rows, collapse = "\n"))
}

html_config_box <- function(config) {
  items <- vapply(names(config), function(nm) {
    sprintf("<strong>%s:</strong> <code>%s</code>", nm, as.character(config[[nm]]))
  }, character(1))
  sprintf('<div class="config-box">%s</div>\n', paste(items, collapse = " &nbsp;|&nbsp; "))
}


