generate_combined_report <- function() {
  out <- file.path(output_dir, "combined_validation.html")

  # Read all available results
  sections <- list()
  overall_total <- 0
  overall_passed <- 0
  overall_failed <- 0

  rds_files <- list(
    centrality = file.path(input_dir, "results_centrality_comprehensive.rds"),
    communities = file.path(input_dir, "results_communities_comprehensive.rds"),
    network_properties = file.path(input_dir, "results_network_properties_comprehensive.rds")
  )

  report_links <- list(
    centrality = "centrality_validation.html",
    communities = "communities_validation.html",
    network_properties = "network_properties_validation.html"
  )

  section_labels <- list(
    centrality = "Centrality Measures",
    communities = "Community Detection",
    network_properties = "Network Properties"
  )

  for (name in names(rds_files)) {
    f <- rds_files[[name]]
    if (file.exists(f)) {
      data <- readRDS(f)
      s <- data$summary
      p <- sum(s$passed)
      fl <- sum(s$failed)
      t <- p + fl
      pct <- if (t > 0) round(100 * p / t, 1) else 0
      sections[[name]] <- list(
        label = section_labels[[name]],
        total = t, passed = p, failed = fl, pct = pct,
        report = report_links[[name]],
        timestamp = data$timestamp,
        config = data$config
      )
      overall_total <- overall_total + t
      overall_passed <- overall_passed + p
      overall_failed <- overall_failed + fl
    }
  }

  if (length(sections) == 0) {
    cat("  [SKIP] No result files found for combined report\n")
    return(invisible(NULL))
  }

  overall_pct <- if (overall_total > 0) round(100 * overall_passed / overall_total, 1) else 0

  html <- paste0(
    html_head("cograph Validation Dashboard"),
    html_header("cograph Comprehensive Validation Dashboard",
                sprintf("Combined results from all validation suites | %s",
                        format(Sys.time(), "%Y-%m-%d %H:%M:%S"))),
    html_summary_cards(overall_total, overall_passed, overall_failed),
    "<h2>Section Reports</h2>\n"
  )

  # Section summary table
  section_df <- do.call(rbind, lapply(sections, function(s) {
    data.frame(
      Suite = s$label,
      Total = s$total,
      Passed = s$passed,
      Failed = s$failed,
      pass_rate = sprintf("%.1f%%", s$pct),
      stringsAsFactors = FALSE
    )
  }))
  html <- paste0(html, html_table(section_df, highlight_col = "pass_rate"))

  # Links to individual reports
  html <- paste0(html, '<div style="text-align:center; margin: 20px 0;">\n')
  for (s in sections) {
    html <- paste0(html, sprintf('<a class="section-link" href="%s">%s Report</a>\n',
                                 s$report, s$label))
  }
  html <- paste0(html, "</div>\n")

  # Per-section detail
  for (name in names(sections)) {
    s <- sections[[name]]
    f <- rds_files[[name]]
    data <- readRDS(f)

    html <- paste0(html, sprintf("<h2>%s</h2>\n", s$label))

    if (!is.null(data$config)) {
      html <- paste0(html, html_config_box(data$config))
    }

    # Show the per-measure summary table
    display_cols <- intersect(
      c("measure", "test_group", "category", "validated_against",
        "passed", "failed", "max_diff", "pass_rate"),
      names(data$summary)
    )
    html <- paste0(html, html_table(data$summary[, display_cols, drop = FALSE],
                                    highlight_col = "pass_rate"))

    # Show up to 10 failures
    if (length(data$failures) > 0) {
      n_show <- min(10, length(data$failures))
      fail_items <- vapply(data$failures[seq_len(n_show)], function(f) {
        sprintf("<li><strong>%s</strong> (test %s): %s</li>",
                if (!is.null(f$measure)) f$measure else if (!is.null(f$metric)) f$metric else "unknown",
                f$test_id, f$reason)
      }, character(1))
      remaining <- length(data$failures) - n_show
      extra <- if (remaining > 0) sprintf("<li>... and %d more</li>", remaining) else ""
      html <- paste0(html, sprintf("<ul>\n%s\n%s</ul>\n",
                                   paste(fail_items, collapse = "\n"), extra))
    }
  }

  # R session info
  html <- paste0(html,
    "<h2>Environment</h2>\n",
    '<div class="config-box">',
    sprintf("<strong>R:</strong> <code>%s</code> &nbsp;|&nbsp; ", R.version.string),
    sprintf("<strong>igraph:</strong> <code>%s</code> &nbsp;|&nbsp; ",
            tryCatch(as.character(packageVersion("igraph")), error = function(e) "N/A")),
    sprintf("<strong>cograph:</strong> <code>%s</code> &nbsp;|&nbsp; ",
            tryCatch(as.character(packageVersion("cograph")), error = function(e) "dev")),
    sprintf("<strong>Platform:</strong> <code>%s</code>",
            paste(Sys.info()[c("sysname", "release")], collapse = " ")),
    "</div>\n"
  )

  html <- paste0(html, html_tail())
  writeLines(html, out)
  cat("  [OK] Combined dashboard:", out, "\n")
  invisible(out)
}


